def choose_gem(month)
  if month=="January"
    gem="Garnet"
  elsif month=="February"
    gem="Amethyst"
  elsif month=="march"
    gem="aquamarine"
  elsif month=="april"
    gem="diamond"
  elsif month=="may"
    gem="emerald"
  elsif month=="june"
    gem="pearl"
  elsif month=="july"
    gem="ruby"
  elsif month=="august"
    gem="peridot"
  elsif month=="september"
    gem="sapphire"
  elsif month=="october"
    gem="opal"
  elsif month=="november"
    gem="topaz"
  elsif month=="december"
    gem="turquoise"
  
  end

  return gem 
end

